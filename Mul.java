import java.util.Scanner;
class Mul
{
	public static void main(String[] args) 
	{
		Scanner scn = new Scanner(System.in);
		int n = scn.nextInt();
		System.out.println("Enter a num: ");
		for(i=1;i<=n;i++){
			int a = 6;
			int n = 0;
			n=a*i;
		System.out.println(a+ "X" +i+ "=" +n);
		}
	}
}
